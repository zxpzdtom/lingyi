export type I18nKey = keyof typeof messages['zh-CN']

export function t(uiLanguage: UiLanguage | undefined, key: I18nKey, vars?: Record<string, string | number>) {
  const lang = uiLanguage === 'en-US' ? 'en-US' : 'zh-CN'
  let value: string = messages[lang][key]
  if (!vars) return value
  for (const [name, replacement] of Object.entries(vars)) {
    value = value.replaceAll(`{{${name}}}`, String(replacement))
  }
  return value
}

export function displayLanguage(language: string, uiLanguage: UiLanguage | undefined) {
  if (language === '中文') return t(uiLanguage, 'languageChinese')
  if (language === '自动检测') return t(uiLanguage, 'languageAuto')
  return language
}

export function compactLanguage(language: string, uiLanguage: UiLanguage | undefined) {
  return displayLanguage(language, uiLanguage).replace(' (简体)', '').replace(' (Simplified)', '')
}

export function providerLabel(providerId: ProviderId, uiLanguage: UiLanguage | undefined) {
  if (providerId === 'chromeBuiltIn') return t(uiLanguage, 'localGeminiTab')
  if (providerId === 'compatible') return uiLanguage === 'en-US' ? 'Custom compatible endpoint' : '自定义兼容端点'
  return providerId === 'openrouter' ? 'OpenRouter' : providerId === 'gemini' ? 'Gemini' : 'OpenAI'
}

export function providerHint(providerId: ProviderId, uiLanguage: UiLanguage | undefined) {
  const english = uiLanguage === 'en-US'
  if (providerId === 'chromeBuiltIn') return english ? 'Built-in local model. First use requires a download and no API key.' : '浏览器内置本地模型，首次使用需要下载，不需要 API Key。'
  if (providerId === 'openai') return english ? 'Official OpenAI API, suitable for reliable translation and summaries.' : '官方 OpenAI API，适合稳定翻译与总结。'
  if (providerId === 'openrouter') return english ? 'Aggregates many models. Model names usually include the provider prefix.' : '聚合多模型，模型名通常包含提供商前缀。'
  if (providerId === 'gemini') return english ? 'Uses Gemini through the OpenAI-compatible API.' : '使用 Gemini 的 OpenAI-compatible 接口。'
  return english ? 'For One API, LiteLLM, compatible proxies, or private gateways.' : '用于 One API、LiteLLM、兼容代理或私有网关。'
}

export function themeDescription(themeId: ThemeId, uiLanguage: UiLanguage | undefined) {
  if (uiLanguage !== 'en-US') {
    if (themeId === 'lucid') return '干净中性'
    if (themeId === 'halo') return '暖陶纸感'
    return '暖纸友好'
  }
  if (themeId === 'lucid') return 'Clean neutral'
  if (themeId === 'halo') return 'Warm clay'
  return 'Warm paper'
}
